class Song:
    def __init__(self, id, title, genre, artist, year, views,
                 features, lyrics, lang_cld3, lang_ft, language):
        self.id = id
        self.title = title
        self.genre = genre
        self.artist = artist
        self.year = year
        self.views = views
        self.features = features
        self.lyrics = lyrics
        self.lang_cld3 = lang_cld3
        self.lang_ft = lang_ft
        self.language = language
